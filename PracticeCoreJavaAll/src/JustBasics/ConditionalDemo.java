package JustBasics;

public class ConditionalDemo {

	public static void main(String[] args) {

		int marks = 60;

		if (marks >= 80) {
			System.out.println("A");
		} else if (marks >= 50) {
			System.out.println("B");
		} 
		
		
		if (marks >= 40) {
			System.out.println("C");
		} else {
			System.out.println("D");
		}

		System.out.println("bye");

	}

}
